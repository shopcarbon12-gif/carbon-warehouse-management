/**
 * Writes gitignored `.env.local` so local dev uses **real** Lightspeed/Shopify/etc. credentials
 * (from `.env.coolify.local`) while keeping **local** public URLs and **local** Postgres.
 *
 * Run after:
 *   docker compose up -d
 *   npm run db:mirror:ssh   ← production data in local `carbon_wms`
 *
 *   npm run env:parity-local
 *
 * Sets `WMS_LS_STRICT=1` (no silent simulated Lightspeed catalog) and hides the local-db banner.
 * Does **not** copy `DATABASE_URL` from Coolify — local stays `localhost:5432/carbon_wms`.
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const coolifyPath = path.join(root, ".env.coolify.local");
const target = path.join(root, ".env.local");

const LOCAL_ORIGIN = "http://localhost:3040";
const LOCAL_DB = "postgresql://postgres:postgres@localhost:5432/carbon_wms";

/** @param {string} p */
function loadDotenvFile(p) {
  const out = {};
  if (!fs.existsSync(p)) return out;
  let text = fs.readFileSync(p, "utf8");
  if (text.charCodeAt(0) === 0xfeff) text = text.slice(1);
  for (const line of text.split(/\r?\n/)) {
    const t = line.trim();
    if (!t || t.startsWith("#")) continue;
    const i = t.indexOf("=");
    if (i <= 0) continue;
    const key = t.slice(0, i).trim();
    let val = t.slice(i + 1).trim();
    if (
      (val.startsWith('"') && val.endsWith('"')) ||
      (val.startsWith("'") && val.endsWith("'"))
    ) {
      val = val.slice(1, -1);
    }
    out[key] = val;
  }
  return out;
}

/** Prefixes: pull integration secrets from Coolify snapshot into local. */
const PREFIXES = ["LS_", "SHOPIFY_", "R2_", "RESEND_", "PUSH_NOTIFICATION_"];

/** Exact keys (no prefix match). */
const EXTRA_KEYS = new Set([
  "SESSION_SECRET",
  "WMS_DEVICE_KEY",
  "EDGE_API_KEY",
  "WMS_EDGE_INGEST_KEY",
]);

function shouldCopyKey(key) {
  if (EXTRA_KEYS.has(key)) return true;
  for (const pre of PREFIXES) {
    if (key.startsWith(pre)) return true;
  }
  return false;
}

/** @param {string} v */
function escapeEnvVal(v) {
  if (/[\s#"'\n\\]/.test(v)) return `"${v.replace(/\\/g, "\\\\").replace(/"/g, '\\"')}"`;
  return v;
}

const coolify = loadDotenvFile(coolifyPath);
if (Object.keys(coolify).length === 0) {
  console.error("Missing or empty .env.coolify.local — add a gitignored copy of Coolify env keys first.");
  process.exit(1);
}

const pulled = {};
for (const [k, v] of Object.entries(coolify)) {
  if (!shouldCopyKey(k)) continue;
  pulled[k] = v;
}

const lines = [
  "# Carbon WMS — production parity for LOCAL dev (gitignored). Generated by npm run env:parity-local",
  "# Requires: docker compose + npm run db:mirror:ssh for real DB data.",
  "",
  `DATABASE_URL=${LOCAL_DB}`,
  `WMS_APP_PUBLIC_BASE_URL=${LOCAL_ORIGIN}`,
  `NEXT_PUBLIC_BASE_URL=${LOCAL_ORIGIN}`,
  `LS_REDIRECT_URI=${LOCAL_ORIGIN}/api/lightspeed/callback`,
  `SHOPIFY_REDIRECT_URI=${LOCAL_ORIGIN}/api/shopify/callback`,
  "WMS_HIDE_LOCAL_DB_BANNER=1",
  "WMS_PRODUCTION_PARITY_LOCAL=1",
  "WMS_LS_STRICT=1",
  "",
  "# --- copied from .env.coolify.local (integration secrets) ---",
];

const BLOCK = new Set([
  "DATABASE_URL",
  "COOLIFY_DEPLOY_WEBHOOK_URL",
  "COOLIFY_API_TOKEN",
  "LS_REDIRECT_URI",
  "SHOPIFY_REDIRECT_URI",
  "NEXT_PUBLIC_BASE_URL",
  "WMS_APP_PUBLIC_BASE_URL",
  "WMS_HIDE_LOCAL_DB_BANNER",
  "WMS_PRODUCTION_PARITY_LOCAL",
  "WMS_LS_STRICT",
  "WMS_AUTO_MIGRATE",
  "WMS_AUTO_SEED",
]);

for (const k of Object.keys(pulled).sort()) {
  if (BLOCK.has(k) || k.startsWith("COOLIFY_")) continue;
  lines.push(`${k}=${escapeEnvVal(String(pulled[k] ?? ""))}`);
}

const out = `${lines.join("\n")}\n`;
fs.writeFileSync(target, out, "utf8");

console.log("Wrote", target);
console.log("  DATABASE_URL -> local docker (run db:mirror:ssh if you need prod data)");
console.log("  Public URLs + LS_/Shopify redirects ->", LOCAL_ORIGIN);
console.log("  WMS_LS_STRICT=1 (Lightspeed catalog will not silently use demo SKUs)");
console.log(`  Pulled ${Object.keys(pulled).length} keys from .env.coolify.local (minus redirects/COOLIFY_*)`);
